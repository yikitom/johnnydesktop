export async function POST(req: NextRequest) {
  const { sourceIds, applicationId } = await req.json();

  const sourceNames: Record<string, string> = {
    xiaohongshu: '小红书',
    tourism: '旅游局数据',
    consultation: '用户咨询数据',
    search: '用户查询数据',
    visit: '用户访问数据',
  };

  const sources = sourceIds.map((id: string) => sourceNames[id] || id);
  const generator = reportTemplates[applicationId];

  if (!generator) {
    return NextResponse.json({ error: 'Unknown application' }, { status: 400 });
  }

  const result = generator(sources);
  return NextResponse.json({ result });
}
